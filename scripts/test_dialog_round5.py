for name, msgs in SCENARIOS.items():
    print(f"\n{'='*60}")
    print(f"  {name}")
    print(f"{'='*60}")
    bot = SalesBot(llm=_llm, flow_name='autonomous')
    for i, msg in enumerate(msgs, 1):
        t0 = time.time()
        r = bot.process(msg)
        dt = time.time() - t0
        resp = r.get('response', '')
        state = r.get('state', '')
        intent = r.get('intent', '')
        print(f"  T{i} [{state}] ({dt:.1f}s) {intent}:")
        print(f"    {resp}")
        low = resp.lower()
        issues = []
        if 'демо' in low: issues.append('ДЕМО')
        if re.search(r'(?<!\w)(рад|понял|готов|подобрал)\b', low): issues.append('МУЖ.РОД')
        if '**' in resp: issues.append('MD')
        if len(resp.split()) > 60: issues.append(f'ДЛИНА:{len(resp.split())}')
        if 'нет демо' in low or 'нет тестов' in low: issues.append('НЕТ ДЕМО')
        if re.search(r'круглосуточн|24\s*/?\s*7', low): issues.append('24/7')
        # Check for "менеджер" without "коллега" context
        if 'менеджер свяжется' in low: issues.append('МЕНЕДЖЕР СВЯЖЕТСЯ (должно быть коллега)')
        if issues:
            print(f"    >>> {issues}")
    print()
