    print(f"{'='*80}")

    total_start = time.time()
    for scenario in SCENARIOS:
        print(f"\n>>> [{scenario['id']}] {scenario['name']} ({len(scenario['msgs'])} сообщений)...")
        dialog = run_dialog(scenario, bot)
        formatted = format_dialog(dialog)
        print(formatted)
        all_results.append(dialog)

    total_elapsed = round(time.time() - total_start, 2)

    # Считаем статистику
    total_turns = sum(d["num_turns"] for d in all_results)
    avg_turn_time = round(
        sum(t["elapsed_s"] for d in all_results for t in d["turns"]) / total_turns, 2
    ) if total_turns > 0 else 0
    finals = sum(1 for d in all_results if any(t.get("is_final") for t in d["turns"]))

