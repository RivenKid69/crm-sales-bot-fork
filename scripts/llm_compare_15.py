def compare(file_a: str, file_b: str):
    """Сравнивает два JSON-файла с результатами."""
    with open(file_a, encoding="utf-8") as f:
        data_a = json.load(f)
    with open(file_b, encoding="utf-8") as f:
        data_b = json.load(f)

    sa = data_a["summary"]
    sb = data_b["summary"]

    print(f"\n{'='*80}")
    print(f"  СРАВНЕНИЕ: {sa['label'].upper()} vs {sb['label'].upper()}")
    print(f"{'='*80}\n")

    # Таблица метрик
    metrics = [
        ("Всего ходов", sa["total_turns"], sb["total_turns"]),
        ("Среднее время хода (s)", sa["avg_turn_time_s"], sb["avg_turn_time_s"]),
        ("Общее время (s)", sa["total_time_s"], sb["total_time_s"]),
        ("Финалы", sa["finals_reached"], sb["finals_reached"]),
        ("Верификатор: accepted", sa["verifier_accepted"], sb["verifier_accepted"]),
        ("Верификатор: rewritten", sa["verifier_rewritten"], sb["verifier_rewritten"]),
    ]

    header = f"{'Метрика':<30} | {sa['label']:>12} | {sb['label']:>12} | {'Δ':>12}"
    print(header)
    print("-" * len(header))
    for name, va, vb in metrics:
        if isinstance(va, float):
            delta = round(vb - va, 2)
            sign = "+" if delta > 0 else ""
            print(f"{name:<30} | {va:>12.2f} | {vb:>12.2f} | {sign}{delta:>11.2f}")
        else:
            delta = vb - va
            sign = "+" if delta > 0 else ""
            print(f"{name:<30} | {va:>12} | {vb:>12} | {sign}{delta:>11}")

    # Сравнение по сценариям
    print(f"\n{'='*80}")
    print("  ПО СЦЕНАРИЯМ")
    print(f"{'='*80}\n")

    scenarios_a = {s["id"]: s for s in data_a["scenarios"]}
    scenarios_b = {s["id"]: s for s in data_b["scenarios"]}

    print(f"{'ID':<5} | {'Фокус':<25} | {sa['label']+' время':>14} | {sb['label']+' время':>14} | {'Speedup':>8}")
    print("-" * 80)
    for sid in sorted(set(list(scenarios_a.keys()) + list(scenarios_b.keys()))):
        da = scenarios_a.get(sid)
        db = scenarios_b.get(sid)
        if da and db:
            focus = da["llm_focus"][:25]
            ta = da["total_time_s"]
            tb = db["total_time_s"]
            speedup = round(ta / tb, 2) if tb > 0 else "N/A"
            print(f"{sid:<5} | {focus:<25} | {ta:>12.1f}s | {tb:>12.1f}s | {speedup:>7}x")

    # Подробное сравнение ответов — покажем по одному ходу из каждого сценария
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    compare_path = Path("results") / f"llm_compare_diff_{sa['label']}_vs_{sb['label']}_{ts}.md"

    md = [
        f"# Сравнение: {sa['label']} vs {sb['label']}",
        f"## {datetime.now().strftime('%Y-%m-%d %H:%M')}",
        "",
    ]

    for sid in sorted(scenarios_a.keys()):
        da = scenarios_a.get(sid)
        db = scenarios_b.get(sid)
        if not da or not db:
            continue

        md.append(f"\n## {sid}: {da['name']} (фокус: {da['llm_focus']})")
        md.append("")
        md.append("### Контрольные точки")
        for cp in da["check_points"]:
            md.append(f"- {cp}")
        md.append("")

        max_turns = max(len(da["turns"]), len(db["turns"]))
        for ti in range(max_turns):
            ta = da["turns"][ti] if ti < len(da["turns"]) else None
            tb = db["turns"][ti] if ti < len(db["turns"]) else None

            user_msg = (ta or tb)["user"]
            md.append(f"**Ход {ti+1}** | Клиент: `{user_msg}`")
            md.append("")

            if ta:
                md.append(f"**{sa['label']}** ({ta['elapsed_s']}s, state={ta['state']}, intent={ta['intent']}):")
                md.append(f"> {ta['bot']}")
                md.append("")

            if tb:
                md.append(f"**{sb['label']}** ({tb['elapsed_s']}s, state={tb['state']}, intent={tb['intent']}):")
                md.append(f"> {tb['bot']}")
                md.append("")

            md.append("---")

    with open(compare_path, "w", encoding="utf-8") as f:
        f.write("\n".join(md))

    print(f"\n  Подробное сравнение: {compare_path}")


