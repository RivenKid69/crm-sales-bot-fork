def run_scenario(bot: SalesBot, name: str, messages: List[str]) -> Dict[str, Any]:
    """Прогнать один сценарий, вернуть трейс."""
    bot.reset()
    trace = []
    for idx, msg in enumerate(messages, 1):
        t0 = time.time()
        result = bot.process(msg)
        elapsed = time.time() - t0
        trace.append({
            "turn": idx,
            "user": msg,
            "bot": result.get("response", ""),
            "state": result.get("state", ""),
            "intent": result.get("intent", ""),
            "action": result.get("action", ""),
            "spin_phase": result.get("spin_phase", ""),
            "lead_score": result.get("lead_score", 0),
            "is_final": result.get("is_final", False),
            "elapsed_s": round(elapsed, 2),
        })
        if result.get("is_final"):
            break
    return {"scenario": name, "trace": trace}


