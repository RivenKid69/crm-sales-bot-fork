DIVIDER = "─" * 80


def run_dialog(scenario: dict, bot: SalesBot) -> dict:
    """Запускает один диалог, собирает все метаданные."""
    bot.reset()
    turns = []
    start = time.time()

    for i, msg in enumerate(scenario["msgs"]):
        t0 = time.time()
        result = bot.process(msg)
        elapsed = time.time() - t0

        turn_data = {
            "turn": i + 1,
            "user": msg,
            "bot": result["response"],
            "state": result.get("state", "?"),
            "action": result.get("action", "?"),
            "spin_phase": result.get("spin_phase", "?"),
            "intent": result.get("intent", "?"),
            "template_key": result.get("template_key", "?"),
            "is_final": result.get("is_final", False),
            "confidence": result.get("confidence", 0),
            "elapsed_s": round(elapsed, 2),
        }

        # Собираем extracted_data если есть
        ed = result.get("extracted_data")
        if ed and isinstance(ed, dict):
            non_empty = {k: v for k, v in ed.items() if v}
            if non_empty:
                turn_data["extracted_data"] = non_empty

        # Собираем метаданные верификатора
        vm = result.get("_factual_verifier_meta")
        if vm and isinstance(vm, dict):
            turn_data["verifier_meta"] = vm

        turns.append(turn_data)
        if result.get("is_final"):
            break

    total_time = round(time.time() - start, 2)
    return {
        "id": scenario["id"],
        "name": scenario["name"],
        "check_points": scenario["check_points"],
        "turns": turns,
        "total_time_s": total_time,
        "num_turns": len(turns),
    }


def format_dialog(d: dict) -> str:
    """Форматирует диалог для текстового отчёта."""
    lines = [
        f"\n{'='*80}",
        f"  {d['id']} | {d['name']}",
        f"  Ходов: {d['num_turns']} | Время: {d['total_time_s']}s",
        f"{'='*80}",
        "",
        "  Контрольные точки (для ручной проверки):",
    ]
    for cp in d["check_points"]:
        lines.append(f"    • {cp}")
    lines.append("")

    for t in d["turns"]:
        lines.append(DIVIDER)
        meta = f"T{t['turn']} | state={t['state']} | action={t['action']} | spin={t['spin_phase']} | intent={t['intent']} | conf={t['confidence']:.2f} | {t['elapsed_s']}s"
        lines.append(f"  [{meta}]")
        if t.get("template_key") and t["template_key"] != "?":
            lines.append(f"  template: {t['template_key']}")
        lines.append(f"  КЛИЕНТ: {t['user']}")
        lines.append(f"  БОТ:    {t['bot']}")
        if t.get("extracted_data"):
            lines.append(f"  📋 Извлечено: {t['extracted_data']}")
        if t.get("verifier_meta"):
            vm = t["verifier_meta"]
            lines.append(f"  🔍 Верификатор: verdict={vm.get('verdict','?')} codes={vm.get('reason_codes',[])}")
        if t["is_final"]:
            lines.append("  ✅ ФИНАЛ")
        lines.append("")

    return "\n".join(lines)


def main():
    setup_autonomous_pipeline()

    llm = OllamaLLM()
    bot = SalesBot(llm, flow_name="autonomous")

    all_results = []
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    json_path = Path(__file__).parent.parent / "results" / f"deep_audit_10_{ts}.json"
    md_path = Path(__file__).parent.parent / "results" / f"deep_audit_10_{ts}.md"
    json_path.parent.mkdir(exist_ok=True)

    print(f"\n{'='*80}")
    print(f"  ГЛУБОКИЙ АУДИТ 10 ДИАЛОГОВ — {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    print(f"  Валидаторы: factual_verifier=ON, boundary_validator=ON, llm_judge=ON")
    print(f"{'='*80}")

    for scenario in SCENARIOS:
        print(f"\n>>> [{scenario['id']}] {scenario['name']} ({len(scenario['msgs'])} сообщений)...")
        dialog = run_dialog(scenario, bot)
        formatted = format_dialog(dialog)
        print(formatted)
        all_results.append(dialog)

    # Сохраняем JSON
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump({"timestamp": ts, "scenarios": all_results}, f, ensure_ascii=False, indent=2)

    # Сохраняем MD отчёт
    md_lines = [
        f"# Глубокий аудит 10 диалогов — {datetime.now().strftime('%Y-%m-%d %H:%M')}",
        "",
        "## Сводка",
        "",
        "| ID | Сценарий | Ходов | Время | Финал? |",
        "|----|----------|-------|-------|--------|",
    ]
    for d in all_results:
        final = "да" if any(t.get("is_final") for t in d["turns"]) else "нет"
        md_lines.append(f"| {d['id']} | {d['name']} | {d['num_turns']} | {d['total_time_s']}s | {final} |")

    md_lines.append("")
    md_lines.append("## Диалоги")
    for d in all_results:
        md_lines.append(format_dialog(d))

    with open(md_path, "w", encoding="utf-8") as f:
        f.write("\n".join(md_lines))

    print(f"\n{'='*80}")
    print(f"  Результаты сохранены:")
    print(f"    JSON: {json_path}")
    print(f"    MD:   {md_path}")
    print(f"{'='*80}")


if __name__ == "__main__":
    main()
