            "–Я—А–Є–Љ–µ—А 3 вАФ NO (–∞—А–Є—Д–Љ–µ—В–Є–Ї–∞ –Є–Ј —Д–∞–Ї—В–Њ–≤):\n"
            "–§–Р–Ъ–Ґ–Ђ: ¬ЂMini вАФ 5 000 вВЄ/–Љ–µ—Б –Ј–∞ –Њ–і–љ—Г —В–Њ—З–Ї—Г¬ї\n"
            "–Ю–Ґ–Т–Х–Ґ: ¬Ђ–Ф–ї—П 5 —В–Њ—З–µ–Ї –љ–∞ —В–∞—А–Є—Д–µ Mini —Б—В–Њ–Є–Љ–Њ—Б—В—М —Б–Њ—Б—В–∞–≤–Є—В 25 000 вВЄ –≤ –Љ–µ—Б—П—Ж¬ї\n"
