        # BUT if intent in _RELEVANCE_CHECK_INTENTS вАФ do NOT skip (S06 case)
        if (
            response.rstrip().endswith("?")
            and len(response) < 200
            and not is_relevance_intent
        ):
            return False

        # Skip condition 5: fact_disambiguated вАФ already verified by disambiguation
        if context.get("fact_disambiguated"):
            return False

        user_message = str(context.get("user_message", "") or "").strip()
        if not user_message:
            return False

