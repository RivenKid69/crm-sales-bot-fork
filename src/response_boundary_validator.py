            "–Ч–∞–і–∞—З–∞: —Б—А–∞–≤–љ–Є –Ю–Ґ–Т–Х–Ґ –±–Њ—В–∞ —Б –§–Р–Ъ–Ґ–Р–Ь–Ш –Є–Ј –±–∞–Ј—Л –Ј–љ–∞–љ–Є–є.\n\n"
            "–Я–†–Р–Т–Ш–Ы–Р:\n"
            "- –Т–Њ–њ—А–Њ—Б –Ї –Ї–ї–Є–µ–љ—В—Г, –Њ–±—Й–∞—П —Д—А–∞–Ј–∞ –±–µ–Ј –Ї–Њ–љ–Ї—А–µ—В–љ—Л—Е —Г—В–≤–µ—А–ґ–і–µ–љ–Є–є вЖТ NO.\n"
            "- –Я–µ—А–µ—Б–Ї–∞–Ј, –њ–µ—А–µ—Д—А–∞–Ј–Є—А–Њ–≤–∞–љ–Є–µ –Є–ї–Є –Њ–±–Њ–±—Й–µ–љ–Є–µ –§–Р–Ъ–Ґ–Ю–Т вЖТ NO.\n"
            "- –Р—А–Є—Д–Љ–µ—В–Є–Ї–∞ –Є–Ј –§–Р–Ъ–Ґ–Ю–Т (—Ж–µ–љ–∞ √Ч –Ї–Њ–ї-–≤–Њ, —Б—Г–Љ–Љ–∞ —В–∞—А–Є—Д–Њ–≤) вЖТ NO.\n"
            "- YES —В–Њ–ї—М–Ї–Њ –µ—Б–ї–Є –≤ –Ю–Ґ–Т–Х–Ґ–Х –µ—Б—В—М –Ъ–Ю–Э–Ъ–†–Х–Ґ–Э–Ю–Х —Г—В–≤–µ—А–ґ–і–µ–љ–Є–µ "
            "(—Б–µ—А–≤–Є—Б, –Є–љ—В–µ–≥—А–∞—Ж–Є—П, –Љ–Њ–і—Г–ї—М, —В–∞—А–Є—Д, —Б—А–Њ–Ї, —Ж–µ–љ–∞), "
            "–Ї–Њ—В–Њ—А–Њ–≥–Њ –Т–Ю–Ю–С–©–Х –Э–Х–Ґ –≤ –§–Р–Ъ–Ґ–Р–•.\n\n"
            "–Я–†–Ш–Ь–Х–†–Ђ:\n"
            "–Я—А–Є–Љ–µ—А 1 вАФ YES (–≥–∞–ї–ї—О—Ж–Є–љ–∞—Ж–Є—П):\n"
