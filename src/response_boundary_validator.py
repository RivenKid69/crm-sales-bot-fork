    @staticmethod
    def _history_user_text(context: Dict[str, Any], limit: int = 4) -> str:
        history = context.get("history", [])
        if not isinstance(history, list):
            return ""
        chunks: List[str] = []
        for item in history[-limit:]:
            if isinstance(item, dict):
                user = item.get("user", "")
                if user:
                    chunks.append(str(user))
        return " ".join(chunks)

