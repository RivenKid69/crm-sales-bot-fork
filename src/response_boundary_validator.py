            "–§–Р–Ъ–Ґ–Ђ: ¬Ђ–Ю–±–Њ—А—Г–і–Њ–≤–∞–љ–Є–µ: POS-—В–µ—А–Љ–Є–љ–∞–ї, —Б–Ї–∞–љ–µ—А, –њ—А–Є–љ—В–µ—А —З–µ–Ї–Њ–≤¬ї\n"
