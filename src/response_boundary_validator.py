            "–Ъ–∞–Ј–∞—Е—Б—В–∞–љ–∞, —Г—З—С—В —В–Њ–≤–∞—А–Њ–≤, –њ—А–Њ–і–∞–ґ, —Б–Ї–ї–∞–і–Њ–≤¬ї\n"
