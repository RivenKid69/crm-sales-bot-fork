            "        –Є–ї–Є —Г—В–Њ—З–љ—П—О—Й–Є–є –≤–Њ–њ—А–Њ—Б –Я–Ю –Ґ–Х–Ь–Х (–і–ї—П –њ–Њ–і–±–Њ—А–∞ –Њ—В–≤–µ—В–∞)\n"
            "- false: –Њ—В–≤–µ—В –Њ –Ф–†–£–У–Ю–Ь (–љ–µ –њ–Њ —В–µ–Љ–µ), –Є–ї–Є –њ—Г—Б—В–Њ–є –≤—Б—В—А–µ—З–љ—Л–є "
